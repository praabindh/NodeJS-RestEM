// Single Variable Modules

module.exports.bina = function (a) {
  return a * a;
};

module.exports.cuba = function (a) {
  return a * a * a;
};

module.exports.quada = function (a) {
  return a * a * a * a;
};

module.exports.penta = function (a) {
  return a * a * a * a * a;
};

module.exports.hexa = function (a) {
  return a * a * a * a * a * a;
};

module.exports.septa = function (a) {
  return a * a * a * a * a * a * a;
};

module.exports.octa = function (a) {
  return a * a * a * a * a * a * a * a;
};

module.exports.nona = function (a) {
  return a * a * a * a * a * a * a * a * a;
};

module.exports.deca = function (a) {
  return a * a * a * a * a * a * a * a * a * a;
};

// Double Variable Modules

module.exports.binpow = function (a, b) {
  return (a * a) * b;
};

module.exports.cubpow = function (a, b) {
  return (a * a * a) * b;
};

module.exports.quadpow = function (a, b) {
  return (a * a * a * a) * b;
};

module.exports.pentpow = function (a, b) {
  return (a * a * a * a * a) * b;
};

module.exports.hexpow = function (a, b) {
  return (a * a * a * a * a * a) * b;
};

module.exports.septpow = function (a, b) {
  return (a * a * a * a * a * a * a) * b;
};

module.exports.octpow = function (a, b) {
  return (a * a * a * a * a * a * a * a) * b;
};

module.exports.nonpow = function (a, b) {
  return (a * a * a * a * a * a * a * a * a)* b;
};

module.exports.decpow = function (a, b) {
  return (a * a * a * a * a * a * a * a * a * a) * b;
};